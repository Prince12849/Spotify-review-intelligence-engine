class RecommendationEngine:
    """Rank curated recommendations against the user's current intent."""

    def recommend(self, profile: IntentProfile) -> list[Recommendation]:
        """Return the strongest recommendations for an intent profile."""
        
        # Safely extract language, defaulting to "Auto" if it isn't set
        target_lang = getattr(profile, "language", "Auto")

        # Filter the candidate pool based on language selection
        if target_lang == "Auto":
            candidates = TRACK_LIBRARY
        else:
            candidates = tuple(
                track for track in TRACK_LIBRARY if track.language == target_lang
            )

        # Apply existing scoring logic to the filtered candidates
        ranked = sorted(
            candidates,
            key=lambda item: self._score(item, profile),
            reverse=True,
        )
        return ranked[:MAX_RECOMMENDATIONS]

    def _score(self, item: Recommendation, profile: IntentProfile) -> int:
        """Score one recommendation against intent dimensions."""
        score = 0
        if profile.activity in item.activities:
            score += 4
        if profile.mood in item.moods:
            score += 3
        if profile.energy == item.energy:
            score += 3
        score += len(set(profile.preferred_genres).intersection(set(item.genres))) * 2
        
        # Safe access to discovery_level in case it isn't always present
        discovery_level = getattr(profile, "discovery_level", "Familiar")
        if discovery_level == "Exploratory":
            score += len(item.genres)
        if discovery_level == "Familiar" and profile.energy == item.energy:
            score += 1
            
        return score