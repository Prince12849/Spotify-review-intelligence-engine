"""Mock recommendation engine for DiscoverAI.

The engine is intentionally data-driven and easy to replace with Spotify API,
vector search, or RAG later. For the MVP it ranks curated tracks against an
IntentProfile.
"""

from __future__ import annotations

from dataclasses import dataclass
from urllib.parse import quote_plus

from discover_ai_config import MAX_RECOMMENDATIONS
from discover_ai_intent import IntentProfile


@dataclass(frozen=True)
class Recommendation:
    """A single explainable music recommendation."""

    song_name: str
    artist: str
    language: str
    genres: tuple[str, ...]
    moods: tuple[str, ...]
    activities: tuple[str, ...]
    energy: str
    description: str
    why: str

    @property
    def spotify_search_url(self) -> str:
        """Return a Spotify Search URL with no authentication required."""
        return f"https://open.spotify.com/search/{quote_plus(self.song_name + ' ' + self.artist)}"


